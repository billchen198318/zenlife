package org.qifu.po;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

import org.qifu.base.model.BaseEntity;
import org.qifu.base.model.EntityPK;
import org.qifu.base.model.EntityUK;

@Entity
@Table(
		name="zl_course_read", 
		uniqueConstraints = { 
				@UniqueConstraint( columnNames = {"PERSON_ID", "COURSE_ID"} ) 
		} 
)
public class ZlCourseRead extends BaseEntity<String> implements java.io.Serializable {
	private static final long serialVersionUID = -2631578589593984601L;
	
	private String oid;
	private String personId;
	private String courseId;
	private String readFlag;
	private String cuserid;
	private Date cdate;
	private String uuserid;
	private Date udate;			

	@Override
	@Id
	@EntityPK(name="oid")
	@Column(name="OID")
	public String getOid() {
		return oid;
	}
	@Override
	public void setOid(String oid) {
		this.oid = oid;
	}
	
	@EntityUK(name="personId")
	@Column(name="PERSON_ID")	
	public String getPersonId() {
		return personId;
	}
	
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	
	@EntityUK(name="courseId")
	@Column(name="COURSE_ID")		
	public String getCourseId() {
		return courseId;
	}
	
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	
	@Column(name="READ_FLAG")
	public String getReadFlag() {
		return readFlag;
	}
	
	public void setReadFlag(String readFlag) {
		this.readFlag = readFlag;
	}
	
	@Override
	@Column(name="CUSERID")
	public String getCuserid() {
		return this.cuserid;
	}
	@Override
	public void setCuserid(String cuserid) {
		this.cuserid = cuserid;
	}
	@Override
	@Column(name="CDATE")
	public Date getCdate() {
		return this.cdate;
	}
	@Override
	public void setCdate(Date cdate) {
		this.cdate = cdate;
	}
	@Override
	@Column(name="UUSERID")
	public String getUuserid() {
		return this.uuserid;
	}
	@Override
	public void setUuserid(String uuserid) {
		this.uuserid = uuserid;
	}
	@Override
	@Column(name="UDATE")
	public Date getUdate() {
		return this.udate;
	}
	@Override
	public void setUdate(Date udate) {
		this.udate = udate;
	}		

}
